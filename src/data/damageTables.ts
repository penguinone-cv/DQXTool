// Utility to get damage table for a given temperature, snapping to nearest 50°C
export function getDamageArrayForTemp(temp: number, table: DamageTable = defaultTemperatureDamageTables): number[] {
  // Clamp temp between 50 and 2000
  const clamped = Math.max(50, Math.min(2000, temp));
  // Round to nearest 50
  const snapped = Math.round(clamped / 50) * 50;
  return table[snapped] || table[50];
}
